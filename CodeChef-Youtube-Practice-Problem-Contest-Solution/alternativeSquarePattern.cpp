#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n, k = -4;
    cin >> n;
    for (int i = 1; i <= n; i++)
    {
        if (i % 2 != 0)
        {
            k += 4;
            for (int j = 1; j <= 5; j++)
                cout << ++k << " ";
            cout << endl;
        }
        else
        {
            k += 6;
            for (int j = 1; j <= 5; j++)
                cout << --k << " ";
            cout << endl;
        }
    }
    return 0;
}